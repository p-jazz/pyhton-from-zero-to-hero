# Para agregar elementos al final del arreglo utilizamos el metodo .append()

letters = ["a", "b", "c"]

letters.append("x")
print(letters)

# Para insertar elementos en una posición espécifica utilizamos el metodo .insert()

vowels = ["a", "e", "i", "u"]
# Vamos a insrtar la o en la tercera posición 

vowels.insert(3, 'o')

print("vowels:", vowels)